/* USER CODE BEGIN Application */
void LED_Task(void const * argument)
{
  static portTickType xlastwaketime; /*静态变量，存储上次任务唤醒的时间*/
  const portTickType xfrequent = pdMS_TO_TICKS(1000); 
  /*使用 pdMS_TO_TICKS(1000) 宏将 1000 毫秒转换为相应的 tick 数（即系统的时钟周期）。这样，xfrequent 表示任务每次应该延迟的时间*/
  xlastwaketime = osKernelSysTick();/*获取当前的系统 tick 时间*/
  for(;;)
  {
    osDelayUntil(&xlastwaketime,xfrequent);
    /*会将当前任务阻塞，直到系统时间达到 xlastwaketime + xfrequent，任务将在每个周期结束时被唤醒并继续执行*/
    HAL_GPIO_TogglePin(LED_Green_GPIO_Port,LED_Green_Pin);

  }
}
